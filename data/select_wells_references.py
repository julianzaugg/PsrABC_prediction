#!/usr/bin/env python3
"""
select_wells_references.py

Build a small, pipeline-compatible custom reference FASTA from the Wells et al.
Dryad MopB FASTA files.

The script uses MopBMAGs_50.fasta as the source sequence pool, assigns each
MopBMAGs_50 sequence to a target family by searching for the same sequence ID
in family-specific Wells FASTA files, then selects a fixed number of
representatives per target family.

Default target families:
  - PsrAPhsASrrA
  - TtrASrdA
  - bSreASoeA
  - ArrAArxA

Output FASTA headers follow the same family-first style as the built-in
references generated by 05_fetch_references.py:
  >PsrAPhsASrrA_Wells_<NN>__<original_id>
  >TtrASrdA_Wells_<NN>__<original_id>
  >bSreASoeA_Wells_<NN>__<original_id>
  >ArrAArxA_Wells_<NN>__<original_id>

The first underscore-delimited field is the protein/family/clade anchor used
by 00_pipeline.sh when it auto-generates metadata for --custom-tree-refs.
The companion metadata TSV matches the columns written by 05_fetch_references.py:
  label accession organism protein clade source note status
"""

from __future__ import annotations

import argparse
import os
import re
import sys
from collections import defaultdict, OrderedDict
from pathlib import Path
from typing import Dict, Iterable, List, Tuple


TARGET_FAMILY_FILES = OrderedDict({
    "PsrAPhsASrrA": [
        "PsrAPhsASrrA.fasta",
        "PsrAPhsASrrAwcodes.fasta",
        "PsrAPhsAfinal.fasta",
    ],
    "TtrASrdA": [
        "TtrASrdAfinal.fasta",
        "TtrASrdAarchArrA.fasta",
        "TtrASrdAarchArrAwcodes.fasta",
    ],
    "bSreASoeA": [
        "bSreASoeAfinal.fasta",
        "bSreA.fasta",
        "bSreAwcodes.fasta",
    ],
    "ArrAArxA": [
        "BactArrAArxA.fasta",
        "BactArrAArxAwcodes.fasta",
        "ArxAArrAfinal.fasta",
        "ArxAArrAcodes.fasta",
    ],
})

# Broad aggregate/background files are intentionally ignored for family matching.
AGGREGATE_BASENAMES = {
    "MopBMAGs.fasta",
    "MopBMAGs_50.fasta",
    "MopBMAGs_50aligned.fasta",
    "allDMSORswoutMopaligned.fasta",
    "DMSORcompwoutMop.fasta",
    "MopBMAGs_50aligned.faa",
}


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(
        description="Select Wells et al. MopBMAGs_50 representatives for custom tree references."
    )
    p.add_argument("--wells-dir", required=True,
                   help="Directory containing Wells/Dryad FASTA files, including MopBMAGs_50.fasta")
    p.add_argument("--out-faa", required=True,
                   help="Output FASTA path for selected references")
    p.add_argument("--out-metadata", required=True,
                   help="Output reference metadata TSV path")
    p.add_argument("--per-family", type=int, default=10,
                   help="Number of sequences to select per target family [default: 10]")
    p.add_argument("--source-fasta", default="MopBMAGs_50.fasta",
                   help="Source FASTA within --wells-dir [default: MopBMAGs_50.fasta]")
    p.add_argument("--include-family", action="append", default=None,
                   choices=list(TARGET_FAMILY_FILES.keys()),
                   help="Restrict to one or more target families; may be repeated")
    p.add_argument("--strict", action="store_true",
                   help="Exit non-zero if fewer than --per-family sequences are available for any target family")
    return p.parse_args()


def fasta_iter(path: Path) -> Iterable[Tuple[str, str, str]]:
    """Yield (seq_id, full_header_without_gt, sequence)."""
    header = None
    seq_lines: List[str] = []
    with path.open() as fh:
        for line in fh:
            line = line.rstrip("\n")
            if not line:
                continue
            if line.startswith(">"):
                if header is not None:
                    sid = header.split()[0]
                    yield sid, header, "".join(seq_lines)
                header = line[1:].strip()
                seq_lines = []
            else:
                seq_lines.append(line.strip())
        if header is not None:
            sid = header.split()[0]
            yield sid, header, "".join(seq_lines)


def wrap_seq(seq: str, width: int = 80) -> str:
    return "\n".join(seq[i:i + width] for i in range(0, len(seq), width))


def ids_in_fasta(path: Path) -> set[str]:
    return {sid for sid, _header, _seq in fasta_iter(path)}


def build_family_id_map(wells_dir: Path, families: List[str]) -> Dict[str, List[str]]:
    fam_to_ids: Dict[str, List[str]] = {}
    for family in families:
        ids = set()
        seen_files = []
        for fname in TARGET_FAMILY_FILES[family]:
            path = wells_dir / fname
            if not path.exists():
                continue
            if path.name in AGGREGATE_BASENAMES:
                continue
            seen_files.append(path.name)
            ids.update(ids_in_fasta(path))
        fam_to_ids[family] = sorted(ids)
        if seen_files:
            print(f"[*] {family}: indexed {len(ids)} unique IDs from {', '.join(seen_files)}")
        else:
            print(f"[WARN] {family}: none of the expected family FASTA files were found", file=sys.stderr)
    return fam_to_ids


def select_evenly(records: List[Tuple[str, str, str]], n: int) -> List[Tuple[str, str, str]]:
    """Deterministically select up to n records spread across the sorted list."""
    if len(records) <= n:
        return records
    if n <= 1:
        return [records[0]]
    idxs = []
    for i in range(n):
        idx = round(i * (len(records) - 1) / (n - 1))
        idxs.append(idx)
    # round() can duplicate indexes for small lists; preserve order and fill gaps.
    unique = []
    seen = set()
    for idx in idxs:
        if idx not in seen:
            unique.append(idx)
            seen.add(idx)
    j = 0
    while len(unique) < n and j < len(records):
        if j not in seen:
            unique.append(j)
            seen.add(j)
        j += 1
    return [records[i] for i in sorted(unique[:n])]


def safe_label_part(text: str) -> str:
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", text).strip("_")


def main() -> int:
    args = parse_args()
    wells_dir = Path(args.wells_dir).expanduser().resolve()
    source_path = wells_dir / args.source_fasta
    if not wells_dir.is_dir():
        sys.exit(f"[ERROR] Wells directory not found: {wells_dir}")
    if not source_path.exists():
        sys.exit(f"[ERROR] Source FASTA not found: {source_path}")
    if args.per_family < 1:
        sys.exit("[ERROR] --per-family must be >= 1")

    families = args.include_family or list(TARGET_FAMILY_FILES.keys())
    fam_to_ids = build_family_id_map(wells_dir, families)

    source_records = {sid: (sid, header, seq) for sid, header, seq in fasta_iter(source_path)}
    print(f"[*] Source pool: {len(source_records)} sequences from {source_path.name}")

    selected_rows = []
    selected_fasta_chunks = []
    used_source_ids = set()

    for family in families:
        family_ids = set(fam_to_ids.get(family, []))
        matches = [source_records[sid] for sid in sorted(source_records) if sid in family_ids]
        chosen = select_evenly(matches, args.per_family)
        print(f"[*] {family}: {len(matches)} MopBMAGs_50 matches; selected {len(chosen)}")

        if args.strict and len(chosen) < args.per_family:
            sys.exit(f"[ERROR] {family}: only {len(chosen)} matches available; wanted {args.per_family}")

        for i, (sid, header, seq) in enumerate(chosen, start=1):
            if sid in used_source_ids:
                print(f"[WARN] Skipping duplicated source ID across families: {sid}", file=sys.stderr)
                continue
            used_source_ids.add(sid)
            label = f"{family}_Wells_{i:02d}"
            accession = safe_label_part(sid)
            fasta_header = f">{label}__{accession} family={family} original_header={safe_label_part(header)}"
            selected_fasta_chunks.append(f"{fasta_header}\n{wrap_seq(seq)}\n")
            selected_rows.append({
                "label": label,
                "accession": accession,
                "organism": "Wells_et_al_MAG",
                "protein": family,
                "clade": family,
                "source": "Wells_Dryad_MopBMAGs_50",
                "note": f"Selected from {args.source_fasta}; original_header={header}",
                "status": "OK",
            })

    out_faa = Path(args.out_faa)
    out_meta = Path(args.out_metadata)
    out_faa.parent.mkdir(parents=True, exist_ok=True)
    out_meta.parent.mkdir(parents=True, exist_ok=True)

    with out_faa.open("w") as fh:
        for chunk in selected_fasta_chunks:
            fh.write(chunk)

    with out_meta.open("w") as fh:
        fh.write("label\taccession\torganism\tprotein\tclade\tsource\tnote\tstatus\n")
        for row in selected_rows:
            fh.write("\t".join(row[k] for k in [
                "label", "accession", "organism", "protein", "clade", "source", "note", "status"
            ]) + "\n")

    print(f"[DONE] Wrote {len(selected_rows)} selected Wells references")
    print(f"  FASTA   : {out_faa}")
    print(f"  Metadata: {out_meta}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
